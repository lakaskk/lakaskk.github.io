<?php
// เชื่อมต่อฐานข้อมูล
require('connect.php');

// ตรวจสอบว่ามีการส่งไอดีของรายการสั่งซื้อมาหรือไม่
if (isset($_GET['OrID'])) {
    // รับค่าไอดีของรายการสั่งซื้อ
    $OrID = $_GET['OrID'];

    // ค้นหาข้อมูลรายการสั่งซื้อจากฐานข้อมูล
    $sql = "SELECT * FROM ordetail WHERE OrID = $OrID";
    $result = $conn->query($sql);

    if ($result->num_rows > 0) {
        // ดึงข้อมูลรายการสั่งซื้อเป็นแถวเดียว
        $row = $result->fetch_assoc();
        // ส่งข้อมูลในรูปแบบ JSON กลับไปยังหน้าเว็บ
        echo json_encode($row);
    } else {
        // ถ้าไม่พบข้อมูล ส่งข้อความว่า "ไม่พบข้อมูล" กลับไปยังหน้าเว็บ
        echo "ไม่พบข้อมูล";
    }
} else {
    // ถ้าไม่มีไอดีของรายการสั่งซื้อที่ส่งเข้ามา ส่งข้อความว่า "ไม่ได้ระบุไอดี" กลับไปยังหน้าเว็บ
    echo "ไม่ได้ระบุไอดี";
}
?>
